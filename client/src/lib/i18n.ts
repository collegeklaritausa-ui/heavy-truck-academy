// Translation structure type - allows any language's translations
export type Translations = typeof translations;
export type TranslationKeys = Translations[keyof Translations];

// Helper to get localized field from database record
export function getLocalizedField<T extends Record<string, unknown>>(
  record: T,
  fieldPrefix: string,
  lang: Language
): string {
  const langSuffix = lang.charAt(0).toUpperCase() + lang.slice(1);
  const fieldName = `${fieldPrefix}${langSuffix}` as keyof T;
  const fallbackField = `${fieldPrefix}En` as keyof T;
  
  return (record[fieldName] as string) || (record[fallbackField] as string) || '';
}

// Get browser language or default
export function getBrowserLanguage(): Language {
  if (typeof window === 'undefined') return 'en';
  
  const browserLang = navigator.language.split('-')[0];
  const supportedLangs: Language[] = ['en', 'de', 'es', 'fr', 'ar'];
  
  return supportedLangs.includes(browserLang as Language) 
    ? (browserLang as Language) 
    : 'en';
}

// Storage key for language preference
const LANG_STORAGE_KEY = 'preferred_language';

export function getStoredLanguage(): Language | null {
  if (typeof window === 'undefined') return null;
  return localStorage.getItem(LANG_STORAGE_KEY) as Language | null;
}

export function setStoredLanguage(lang: Language): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem(LANG_STORAGE_KEY, lang);
}
